def generate_natal_chart_reading(natal_data):
    """
    Сгенерировать описание натальной карты в стиле Новеллы.
    natal_data — результат astro_service.calculate_natal_chart()
    """
    natal_text = astro_service.format_natal_for_prompt(natal_data)

    prompt = f"""Ты — Новелла, близкая подруга и крутой астролог. Твой стиль: эмпатичный, чуть ироничный, современный и очень поддерживающий. Ты общаешься на «ты», используешь уместные эмодзи и лёгкий сленг (но без перебора).

Задача: Расскажи человеку про его натальную карту. Это его первое знакомство с картой, так что объясни понятно и увлекательно.

Натальная карта:
{natal_text}

Структура ответа:
1. Приветствие — тёплое, вовлекающее.
2. Большая тройка (Солнце, Луна, Асцендент) — объясни каждый просто и ярко, что это значит для его личности.
3. Ключевые особенности — выдели 2-3 интересных момента из расположения планет (сильные стороны, таланты, зоны роста).
4. Итог — вдохновляющее завершение.

Важно:
- Пиши на русском языке.
- НЕ используй markdown-разметку (никаких ** или ## или *). Используй эмодзи для выделения.
- Пиши так, будто записываешь голосовое сообщение.
- Объём: 300-400 слов."""

    response = client.chat.completions.create(
        model="gpt-4o",
        messages=[{"role": "user", "content": prompt}],
        max_tokens=1500,
        temperature=0.8
    )
    return response.choices[0].message.content


def generate_horoscope(birth_date, birth_time, birth_place):
    """
    Сгенерировать персональный прогноз на день.
    Использует реальные позиции планет из натальной карты и текущих транзитов.
    """
    # Рассчитываем натальную карту и текущие транзиты
    natal_data = astro_service.calculate_natal_chart(birth_date, birth_time, birth_place)
    transits = astro_service.get_current_transits()

    natal_text = astro_service.format_natal_for_prompt(natal_data)
    transits_text = astro_service.format_transits_for_prompt(transits)

    prompt = f"""Ты — Новелла, близкая подруга и крутой астролог. Твой стиль: эмпатичный, чуть ироничный, современный и очень поддерживающий. Ты общаешься на «ты», используешь уместные эмодзи и лёгкий сленг (но без перебора).

Задача: Составь персональный «Звёздный чек-ап» на сегодня.

Натальная карта пользователя:
{natal_text}

{transits_text}

Проанализируй, какие транзитные планеты сейчас взаимодействуют с натальными позициями. Обращай внимание на соединения, оппозиции, квадратуры и тригоны (орбис 5-8 градусов).

Структура ответа:
1. Приветствие — коротко и ярко.
2. Вайб дня — общая энергия дня через призму транзитов к натальной карте. Укажи конкретные аспекты (например: «Транзитная Луна проходит по твоему натальному Юпитеру — день для удачи!»).
3. Love & Relations — без клише, жизненно.
4. Work & Money — фокус на продуктивности или отдыхе.
5. Self-care — что сделать для здоровья и настроения.
6. Главный совет дня — одна мощная фраза-напутствие.

Важно:
- Пиши на русском языке.
- НЕ используй markdown-разметку (никаких ** или ## или *). Используй эмодзи для выделения секций.
- Даже сложные аспекты подавай как «время для выводов», не пугай.
- Пиши так, будто записываешь «кружочек» в телеграме.
- Объём: 250-350 слов."""

    response = client.chat.completions.create(
        model="gpt-4o",
        messages=[{"role": "user", "content": prompt}],
        max_tokens=1200,
        temperature=0.8
    )
    return response.choices[0].message.content


def generate_tarot_reading(cards, question, birth_date=None, birth_time=None,
                           birth_place=None, spread_type="past_present_future",
                           category=None):
    """
    Сгенерировать интерпретацию расклада Таро в стиле «Астро-Таро».
    spread_type: "past_present_future" или "single_card"
    category: "relationships", "work_money", "self_discovery" (для single_card)
    """
    cards_text = ", ".join(cards)

    # Если есть данные рождения — добавляем астро-контекст
    astro_context = ""
    if birth_date and birth_time and birth_place:
        natal_data = astro_service.calculate_natal_chart(birth_date, birth_time, birth_place)
        transits = astro_service.get_current_transits()
        astro_context = f"""

Астро-контекст клиента:
Солнце: {natal_data['sun_sign']}, Луна: {natal_data['moon_sign']}, Асцендент: {natal_data['ascendant']}

{astro_service.format_transits_for_prompt(transits)}

Используй эту информацию, чтобы связать значение карт с астрологическим профилем клиента. Если видишь перекличку между картой и транзитом — обязательно подсвети: «Это не случайность, это подтверждение того, что говорят звёзды»."""

    # Разные промпты для разных типов раскладов
    if spread_type == "past_present_future":
        spread_instruction = f"""Расклад «Прошлое — Настоящее — Будущее»:
- Карта 1 ({cards[0]}): Прошлое — что привело к текущей ситуации.
- Карта 2 ({cards[1]}): Настоящее — что происходит в моменте и на что забить.
- Карта 3 ({cards[2]}): Будущее — наиболее вероятный исход, если ничего не менять."""
    else:
        # Расклад по категории
        category_names = {
            "relationships": "Отношения",
            "work_money": "Работа и деньги",
            "self_discovery": "Самопознание",
        }
        cat_name = category_names.get(category, "Общее")
        spread_instruction = f"""Расклад по теме «{cat_name}»:
- Карта 1 ({cards[0]}): Ситуация — что сейчас происходит в этой сфере.
- Карта 2 ({cards[1]}): Препятствие — что мешает или на что обратить внимание.
- Карта 3 ({cards[2]}): Совет — что делать, чтобы всё стало лучше."""

    prompt = f"""Ты — Новелла, твоя интуиция острая, а советы всегда в яблочко. Ты не просто толкуешь карты, ты видишь их связь с личностью человека.

Задача: Разбери расклад Таро.

Вопрос клиента: {question}
Карты: {cards_text}
{astro_context}

{spread_instruction}

Как писать:
- Забудь про фразы «карта символизирует». Пиши: «Смотри, [Карта] тут вообще не случайно. Это значит, что...».
- Если есть астро-контекст — смешивай значения карт с астро-вайбом юзера.
- Финал: Дай четкий совет-действие. Что сделать прямо сейчас?

Важно:
- Пиши на русском языке.
- НЕ используй markdown-разметку (никаких ** или ## или *). Используй эмодзи для выделения.
- Стиль: дружеский, мистический, но приземленный. Максимум пользы, минимум «воды».
- Пиши так, будто записываешь голосовое.
- Объём: 300-400 слов."""

    response = client.chat.completions.create(
        model="gpt-4o",
        messages=[{"role": "user", "content": prompt}],
        max_tokens=1500,
        temperature=0.8
    )
    return response.choices[0].message.content
