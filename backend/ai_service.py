def draw_tarot_cards(count=3):
    """Вытянуть случайные карты Таро"""
    return random.sample(TAROT_CARDS, count)


def generate_horoscope(birth_date: str, birth_time: str, birth_place: str):
    """Сгенерировать персональный астрологический прогноз"""
    prompt = f"""Ты — опытный астролог с глубоким знанием натальной астрологии.
Составь персональный прогноз на сегодня для человека с такими данными:
- Дата рождения: {birth_date}
- Время рождения: {birth_time}
- Место рождения: {birth_place}

Учитывай текущие транзиты планет и их аспекты к натальной карте.
Прогноз должен быть конкретным, полезным и вдохновляющим.
Раздели на секции: Общее, Любовь, Карьера, Здоровье.
Пиши на русском языке, в мистическом но тёплом стиле.
Объём: 200-300 слов."""

    response = client.chat.completions.create(
        model="gpt-4o",
        messages=[{"role": "user", "content": prompt}],
        max_tokens=1000,
        temperature=0.8
    )
    return response.choices[0].message.content


def generate_tarot_reading(cards: list, question: str, birth_date: str = None):
    """Сгенерировать интерпретацию расклада Таро"""
    cards_text = ", ".join(cards)
    birth_info = f"\nДата рождения спрашивающего: {birth_date}" if birth_date else ""

    prompt = f"""Ты — мудрый таролог с многолетним опытом.
Интерпретируй расклад из 3 карт Таро.

Вопрос: {question}
Выпавшие карты: {cards_text}{birth_info}

Расклад «Прошлое — Настоящее — Будущее»:
- Карта 1 ({cards[0]}): Прошлое — что привело к текущей ситуации
- Карта 2 ({cards[1]}): Настоящее — текущая энергия и обстоятельства
- Карта 3 ({cards[2]}): Будущее — к чему ведёт ситуация

Дай глубокую, персональную интерпретацию каждой карты в контексте вопроса.
Заверши общим выводом и советом.
Пиши на русском языке, в мистическом стиле.
Объём: 250-350 слов."""

    response = client.chat.completions.create(
        model="gpt-4o",
        messages=[{"role": "user", "content": prompt}],
        max_tokens=1200,
        temperature=0.8
    )
    return response.choices[0].message.content
